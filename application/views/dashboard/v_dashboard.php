	var latest_periode;
	periode();
	function periode(){
		$.ajax({
			type    : 'ajax',
			url     : '<?php echo base_url();?>index.php/Payment/latest_periode',
			async   : false,
			dataType    : 'json',
			success : function(data){
				var i;
				for(i=0; i<data.length; i++){
					latest_periode = data[i].periode;
				}
				$('#periode').html(latest_periode);
			}
		});
	}

